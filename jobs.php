<script>
  // Fetch the jobs data from data2.json file
  fetch('data2.json')
    .then(response => response.json())
    .then(data => {
      // Get the table body element
      const tableBody = document.querySelector('.widget-26 tbody');

      // Function to filter the jobs based on search criteria
      const filterJobs = () => {
        // Get the search input values
        const titleInput = document.querySelector('.search-1 input').value.toLowerCase();
        const locationInput = document.querySelector('.search-2 input').value.toLowerCase();

        // Clear the table body before adding filtered rows
        tableBody.innerHTML = '';

        // Iterate over the jobs data and create table rows for matching jobs
        data.forEach(job => {
          const title = job.title.toLowerCase();
          const location = job.location.toLowerCase();

          // Check if the job title and location match the search criteria
          if (title.includes(titleInput) && location.includes(locationInput)) {
            const row = document.createElement('tr');

            // Calculate the difference in days between the current date and the submitted date
            const currentDate = new Date();
            const submittedDate = new Date(job.submitted_date);
            const differenceInDays = Math.floor((currentDate - submittedDate) / (1000 * 3600 * 24));

            // Set the class based on the difference in days
            const categoryClass = differenceInDays > 30 ? 'bg-soft-danger' : 'bg-soft-info';

            row.innerHTML = `
              <td>
                ${job.company}
              </td>
              <td>
                <div class="widget-26-job-title">
                  <a href="${job.company_link}">${job.title}</a>
                  <p class="m-0"><a href="${job.company_link}" class="employer-name">${job.company}</a>
                  <span class="text-muted time"></span></p>
                </div>
              </td>
              <td>
                <div class="widget-26-job-info">
                  <p class="text-muted m-0">in <span class="location">${job.location}</span></p>
                </div>
              </td>
              <td>
                <div class="widget-26-job-salary">$ ${job.pay_per_hour}/hr</div>
              </td>
              <td>
                <div class="widget-26-job-category ${categoryClass}">
                  <span>Date Published: ${job.submitted_date}</span>
                </div>
              </td>
            `;

            // Append the row to the table body
            tableBody.appendChild(row);
          }
        });
      };

      // Get the search button
      const searchButton = document.querySelector('.search-2 button');

      // Add event listener to the search button
      searchButton.addEventListener('click', filterJobs);
    })
    .catch(error => console.error(error));
</script>





